-- ============================================================
-- Атрибуция рейсов production_logs конкретному оператору смены.
--
-- КАК ПРИМЕНИТЬ:
--   1. Откройте Supabase Dashboard → SQL Editor
--   2. Вставьте и выполните весь скрипт целиком
--   3. В конце вернётся проверочный запрос
--
-- ЗАЧЕМ: у "Оператора" одна общая учётка на всех (Семён/Максим), поэтому
-- production_logs раньше никак не хранил, кто из них реально оформил рейс.
-- Теперь пишем имя из переключателя "Смена" (operator_shift_settings) при
-- каждой записи — это основа для статистики в карточке "Оператор"
-- (Клиенты → Стафф): объём/рейсы/среднее время загрузки по каждому оператору.
--
-- Старые записи (до внедрения) останутся с operator_name = null — это
-- нормально, статистика по ним просто не считается персонально.
--
-- Скрипт идемпотентен — можно безопасно запускать повторно.
-- ============================================================

alter table public.production_logs
  add column if not exists operator_name text;

comment on column public.production_logs.operator_name is 'Кто из операторов смены (напр. "Семён"/"Максим") зафиксировал рейс — для статистики в карточке "Оператор". Null для записей до внедрения поля.';

create index if not exists idx_production_logs_operator_name on public.production_logs (operator_name);

-- ============================================================
-- Проверочный запрос
-- ============================================================
select column_name, data_type from information_schema.columns
where table_schema = 'public' and table_name = 'production_logs' and column_name = 'operator_name';
