-- ============================================================
-- Список имён операторов смены (Семён/Максим и т.п.) — редактируемый.
--
-- КАК ПРИМЕНИТЬ:
--   1. Откройте Supabase Dashboard → SQL Editor
--   2. Вставьте и выполните весь скрипт целиком
--   3. В конце вернётся проверочная строка
--
-- ЗАЧЕМ: список имён раньше был зашит в коде страницы оператора. Теперь он
-- лежит в той же строке настроек (operator_shift_settings, id=1) и
-- редактируется через карточку "Оператор" на странице Клиенты → Стафф —
-- без правки кода, если завтра сменится состав операторов.
--
-- Скрипт идемпотентен — можно безопасно запускать повторно.
-- ============================================================

alter table public.operator_shift_settings
  add column if not exists available_names jsonb not null default '["Семён", "Максим"]'::jsonb;

comment on column public.operator_shift_settings.available_names is 'Список имён операторов, доступных для выбора на смене (редактируется в карточке "Оператор" на странице Клиенты → Стафф).';

-- Заполняем значением по умолчанию, если строка уже существовала без этой колонки.
update public.operator_shift_settings
set available_names = '["Семён", "Максим"]'::jsonb
where id = 1 and (available_names is null or available_names = '[]'::jsonb);

-- ============================================================
-- Проверочный запрос
-- ============================================================
select * from public.operator_shift_settings where id = 1;
